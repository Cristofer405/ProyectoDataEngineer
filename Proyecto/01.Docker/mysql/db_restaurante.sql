-- -----------------------------------------------------
-- Schema db_restaurante
-- -----------------------------------------------------
CREATE SCHEMA IF NOT EXISTS `db_restaurante` DEFAULT CHARACTER SET utf8mb3 ;

USE `db_restaurante` ;

-- -----------------------------------------------------
-- Table `db_restaurante`.`Categoria`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Categoria` (
  `idCategoria` INT NOT NULL AUTO_INCREMENT,
  `Nombre` VARCHAR(30) NOT NULL,
  PRIMARY KEY (`idCategoria`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`SubCategoria`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`SubCategoria` (
  `idSubCategoria` INT NOT NULL AUTO_INCREMENT,
  `idCategoria` INT NOT NULL,
  `nombre` VARCHAR(50) NOT NULL,
  PRIMARY KEY (`idSubCategoria`),
  INDEX `fk_SubCategoria_Categoria1_idx` (`idCategoria` ASC) VISIBLE,
  CONSTRAINT `fk_SubCategoria_Categoria1`
    FOREIGN KEY (`idCategoria`)
    REFERENCES `db_restaurante`.`Categoria` (`idCategoria`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Carta`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Carta` (
  `idCarta` INT NOT NULL AUTO_INCREMENT,
  `idSubCategoria` INT NOT NULL,
  `Nombre` VARCHAR(50) NOT NULL,
  `Descripcion` VARCHAR(250) NULL DEFAULT NULL,
  `Precio` DECIMAL(10,2) NOT NULL,
  `Estado` BIT(1) NOT NULL DEFAULT b'1',
  PRIMARY KEY (`idCarta`),
  INDEX `fk_Carta_SubCategoria1_idx` (`idSubCategoria` ASC) VISIBLE,
  CONSTRAINT `fk_Carta_SubCategoria1`
    FOREIGN KEY (`idSubCategoria`)
    REFERENCES `db_restaurante`.`SubCategoria` (`idSubCategoria`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Clientes`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Clientes` (
  `idClientes` INT NOT NULL AUTO_INCREMENT,
  `ApePaterno` VARCHAR(40) NULL DEFAULT NULL,
  `ApeMaterno` VARCHAR(40) NULL DEFAULT NULL,
  `Nombres` VARCHAR(40) NULL DEFAULT NULL,
  `Email` VARCHAR(40) NULL DEFAULT NULL,
  `Telefono` VARCHAR(15) NULL DEFAULT NULL,
  PRIMARY KEY (`idClientes`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Distrito`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Distrito` (
  `idDistrito` INT NOT NULL AUTO_INCREMENT,
  `Descripcion` VARCHAR(50) NOT NULL,
  PRIMARY KEY (`idDistrito`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Direcciones`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Direcciones` (
  `idDirecciones` INT NOT NULL AUTO_INCREMENT,
  `Descripcion` VARCHAR(100) NOT NULL,
  `idDistrito` INT NOT NULL,
  `idClientes` INT NOT NULL,
  `Estado` BIT(1) NOT NULL DEFAULT b'1',
  PRIMARY KEY (`idDirecciones`),
  INDEX `fk_Direcciones_Clientes1_idx` (`idClientes` ASC) VISIBLE,
  INDEX `fk_Direcciones_Distrito1_idx` (`idDistrito` ASC) VISIBLE,
  CONSTRAINT `fk_Direcciones_Clientes1`
    FOREIGN KEY (`idClientes`)
    REFERENCES `db_restaurante`.`Clientes` (`idClientes`),
  CONSTRAINT `fk_Direcciones_Distrito1`
    FOREIGN KEY (`idDistrito`)
    REFERENCES `db_restaurante`.`Distrito` (`idDistrito`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Local`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Local` (
  `idLocal` INT NOT NULL AUTO_INCREMENT,
  `NombreLocal` VARCHAR(50) NOT NULL,
  `Direccion` VARCHAR(50) NOT NULL,
  `idDistrito` INT NOT NULL,
  `HorarioAtencion` VARCHAR(20) NOT NULL,
  PRIMARY KEY (`idLocal`),
  INDEX `fk_Local_Distrito1_idx` (`idDistrito` ASC) VISIBLE,
  CONSTRAINT `fk_Local_Distrito1`
    FOREIGN KEY (`idDistrito`)
    REFERENCES `db_restaurante`.`Distrito` (`idDistrito`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`LocalDelivery`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`LocalDelivery` (
  `idLocal` INT NOT NULL,
  `idDistrito` INT NOT NULL,
  INDEX `fk_LocalDelivery_Local1_idx` (`idLocal` ASC) VISIBLE,
  INDEX `fk_LocalDelivery_Distrito1_idx` (`idDistrito` ASC) VISIBLE,
  CONSTRAINT `fk_LocalDelivery_Distrito1`
    FOREIGN KEY (`idDistrito`)
    REFERENCES `db_restaurante`.`Distrito` (`idDistrito`),
  CONSTRAINT `fk_LocalDelivery_Local1`
    FOREIGN KEY (`idLocal`)
    REFERENCES `db_restaurante`.`Local` (`idLocal`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Mesa`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Mesa` (
  `idMesa` INT NOT NULL AUTO_INCREMENT,
  `Descripcion` VARCHAR(10) NOT NULL,
  `Capacidad` TINYINT NULL DEFAULT NULL,
  PRIMARY KEY (`idMesa`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`MesaLocal`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`MesaLocal` (
  `idMesa` INT NOT NULL,
  `dLocal` INT NOT NULL,
  INDEX `fk_table1_Local1_idx` (`dLocal` ASC) VISIBLE,
  INDEX `fk_table1_Mesa1_idx` (`idMesa` ASC) VISIBLE,
  CONSTRAINT `fk_table1_Local1`
    FOREIGN KEY (`dLocal`)
    REFERENCES `db_restaurante`.`Local` (`idLocal`),
  CONSTRAINT `fk_table1_Mesa1`
    FOREIGN KEY (`idMesa`)
    REFERENCES `db_restaurante`.`Mesa` (`idMesa`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Trabajador`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Trabajador` (
  `idTrabajador` INT NOT NULL AUTO_INCREMENT,
  `ApellidosNombres` VARCHAR(120) NOT NULL,
  `idLocal` INT NOT NULL,
  PRIMARY KEY (`idTrabajador`),
  INDEX `fk_Trabajador_Local1_idx` (`idLocal` ASC) VISIBLE,
  CONSTRAINT `fk_Trabajador_Local1`
    FOREIGN KEY (`idLocal`)
    REFERENCES `db_restaurante`.`Local` (`idLocal`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`Pedidos`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `db_restaurante`.`Pedidos` (
  `idPedidos` INT NOT NULL AUTO_INCREMENT,
  `idLocal` INT NOT NULL,
  `idMesa` INT NOT NULL,
  `idClientes` INT NOT NULL,
  `idTrabajador` INT NOT NULL,
  `Fecha` DATETIME NOT NULL,
  `TipoConsumo` CHAR(1) NOT NULL,
  `idDireccionEnvio` INT NULL DEFAULT NULL,
  PRIMARY KEY (`idPedidos`),
  INDEX `fk_Pedidos_Local1_idx` (`idLocal` ASC) VISIBLE,
  INDEX `fk_Pedidos_Mesa1_idx` (`idMesa` ASC) VISIBLE,
  INDEX `fk_Pedidos_Clientes1_idx` (`idClientes` ASC) VISIBLE,
  INDEX `fk_Pedidos_Trabajador1_idx` (`idTrabajador` ASC) VISIBLE,
  CONSTRAINT `fk_Pedidos_Clientes1`
    FOREIGN KEY (`idClientes`)
    REFERENCES `db_restaurante`.`Clientes` (`idClientes`),
  CONSTRAINT `fk_Pedidos_Local1`
    FOREIGN KEY (`idLocal`)
    REFERENCES `db_restaurante`.`Local` (`idLocal`),
  CONSTRAINT `fk_Pedidos_Mesa1`
    FOREIGN KEY (`idMesa`)
    REFERENCES `db_restaurante`.`Mesa` (`idMesa`),
  CONSTRAINT `fk_Pedidos_Trabajador1`
    FOREIGN KEY (`idTrabajador`)
    REFERENCES `db_restaurante`.`Trabajador` (`idTrabajador`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `db_restaurante`.`PedidosDetalle`
-- -----------------------------------------------------
