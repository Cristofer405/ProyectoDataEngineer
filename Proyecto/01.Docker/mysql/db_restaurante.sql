  CONSTRAINT `fk_PedidosDetalle_Carta1`
    FOREIGN KEY (`idCarta`)
    REFERENCES `db_restaurante`.`Carta` (`idCarta`),
  CONSTRAINT `fk_PedidosDetalle_Pedidos1`
    FOREIGN KEY (`idPedidos`)
